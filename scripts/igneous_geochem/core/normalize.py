def normalize(data_dict, ref):
    """将元素浓度按参考值标准化，返回 dict。"""
    result = {}
    for k, v in data_dict.items():
        if k in ref and ref[k] > 0 and not np.isnan(v):
            result[k] = v / ref[k]
        else:
            result[k] = np.nan
    return result
